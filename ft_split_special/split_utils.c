int	p_q(char *s1, int i)
{
	if (s1[i] == '"' || s1[i] == '\'')
	{
		i = parseur_quotes(s1, i + 1, s1[i]);
		if (i == -1)
		{
			ft_putstr_fd("Pipe Error\n", 2);
			return (-1);
		}
	}
	return (i);
}
