char	*debug(char *str)
{
	int		n;
	char	*tmp;

	n = ft_strlen(str);
	if (str[0] == '\"' && str[n] == '\"')
	{
		tmp = ft_strtrim(str, "\"");
		return (tmp);
	}
	return (str);
}
